"""Определение сетевых заведений и оценка аналитического потенциала лида."""

from __future__ import annotations

import re

# Известные сети и франшизы в России. Такие заведения обычно уже имеют
# собственный отдел аналитики, поэтому помечаются как низкий потенциал.
KNOWN_CHAINS: list[str] = [
    r"шоколадница", r"кофе хауз", r"правда кофе", r"даблби", r"cofix", r"stars coffee",
    r"one price coffee", r"хлеб насущный", r"волконский", r"буше", r"синнабон", r"цех 85",
    r"теремок", r"крошка картошка", r"додо пицца", r"додо", r"папа джонс", r"домино'с",
    r"subway", r"burger king", r"доминос", r"kfc", r"ростикс", r"вкусвилл", r"пятерочка",
    r"перекресток", r"магнит", r"дикси", r"ашан", r"метро", r"лента", r"окей",
    r"кофемания", r"surf coffee", r"skuratov", r"кофе тайм", r"coffee like",
    r"baggin's", r"кофейня №1", r"поль бейкери", r"север-метрополь", r"коржов",
]

# Скомпилированный паттерн для быстрого поиска совпадений с сетями.
_CHAIN_PATTERN = re.compile("|".join(KNOWN_CHAINS), re.IGNORECASE)


def check_is_chain(name: str | None, brand: str | None) -> bool:
    """Определяет, относится ли заведение к крупной сети или франшизе.

    Считаем сетью, если у объекта в OSM заполнен тег ``brand`` либо название
    совпадает с одной из известных сетей.
    """
    if not name:
        return False

    if brand:
        return True

    return bool(_CHAIN_PATTERN.search(name))


def calculate_potential_score(is_chain: bool, website: str | None, phone: str | None) -> dict:
    """Эвристическая оценка того, насколько заведение перспективно как клиент.

    - HIGH: независимый бизнес с сайтом и телефоном (есть контакты и, скорее всего,
      учётная система, но нет штатного аналитика).
    - MEDIUM: независимый бизнес с единственным каналом связи.
    - LOW: крупная сеть либо полное отсутствие контактов.
    """
    if is_chain:
        return {
            "score": "LOW",
            "reason": "Крупная сеть (обычно уже имеют штатных аналитиков и CRM-департамент)",
            "color": "warning",
        }

    if not website and not phone:
        return {
            "score": "LOW",
            "reason": "Отсутствуют контакты на карте (сложно связаться)",
            "color": "danger",
        }

    if website and phone:
        return {
            "score": "HIGH",
            "reason": (
                "Независимый бизнес с сайтом и телефоном "
                "(есть база контактов, высокая вероятность наличия CRM/POS-системы)"
            ),
            "color": "success",
        }

    return {
        "score": "MEDIUM",
        "reason": "Независимый бизнес, но доступен только один канал связи (телефон или сайт)",
        "color": "primary",
    }
