def _analytics_profile(category_key: str) -> dict[str, str]:
    if category_key in FOOD_CATEGORIES:
        return _ANALYTICS_PROFILES["food"]
    return _ANALYTICS_PROFILES.get(category_key, _ANALYTICS_PROFILES["default"])


