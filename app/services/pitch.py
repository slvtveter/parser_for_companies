def build_template_pitch(
    business_name: str,
    category_key: str,
    pitch_type: str = "analytics",
) -> dict[str, str]:
    """Возвращает тему и тело письма по типу питча (``analytics`` или ``startup``)."""
    if pitch_type == "startup":
        return _startup_pitch(business_name)
    return _analytics_pitch(business_name, category_key)
